        function shouldSkipAlienize(value) {
            return /^#(?:[0-9a-fA-F]{3}|[0-9a-fA-F]{6})$/.test(value)
                || /^var\(--/.test(value)
                || /^rgba?\(/.test(value)
                || /^linear-gradient/.test(value)
                || /^radial-gradient/.test(value)
                || /^color-mix/.test(value)
                || /\.html$/.test(value)
                || /^\d+$/.test(value);
        }

        function alienizeString(value) {
            if (shouldSkipAlienize(value)) return value;
            return value
                .split(/(<[^>]+>)/g)
                .map((chunk) => {
                    if (chunk.startsWith("<") && chunk.endsWith(">")) return chunk;
                    return chunk
                        .replace(/[A-Za-zÁÉÍÓÖŐÚÜŰáéíóöőúüű0-9]/g, (char) => alienGlyphMap[char] || char)
                        .split(/(\s+)/)
                        .map((part) => /\s+/.test(part) ? " ⟡ " : (part.length > 4 ? `${part[0]}${part.slice(1).split("").reverse().join("")}` : part))
                        .join("");
                })
                .join("");
        }

        function alienizeDeep(value) {
            if (Array.isArray(value)) return value.map(alienizeDeep);
            if (value && typeof value === "object") {
                return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, alienizeDeep(item)]));
            }
            if (typeof value === "string") return alienizeString(value);
            return value;
        }

